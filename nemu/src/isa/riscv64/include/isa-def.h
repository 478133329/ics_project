#ifndef __ISA_RISCV64_H__
#define __ISA_RISCV64_H__

#include <common.h>

typedef struct {
  word_t gpr[32];
  vaddr_t pc;
  word_t csr[4];  // mstatus:0x300  mtvec:0x305  mepc:0x341  mcause:0x342
} riscv64_CPU_state;

static inline word_t csr_decode(word_t no) {
    switch (no) {
    case 0x300: return 0;
    case 0x305: return 1;
    case 0x341: return 2;
    case 0x342: return 3;
    default: assert(0);
    }
    return -1;
}

// decode
typedef struct {
  union {
    uint32_t val;
  } inst;
} riscv64_ISADecodeInfo;

#define isa_mmu_check(vaddr, len, type) (MMU_DIRECT)

#endif
